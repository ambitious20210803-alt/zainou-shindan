// 判定: 軸スコアから動物、blockスコアから状態
function diagnose(answers){
  let I=0,E=0,R=0,K=0,block=0;
  answers.forEach((a,i)=>{
    const Q=QUESTIONS[i];
    if(Q.type==="axis"){
      const c=Q.choices[a];
      I+=c.I||0; E+=c.E||0; R+=c.R||0; K+=c.K||0;
    }else{
      block += Q.choices[a].b;
    }
  });
  // 象限決定
  const ie = E>=I ? "E":"I";
  const rk = K>=R ? "K":"R";
  const quad = ie+rk;
  // 象限内で2タイプを枝分かれ：軸の偏り(差)が大きい方を主軸に
  const ieDiff=Math.abs(E-I), rkDiff=Math.abs(K-R);
  // 各象限の2匹: strong="I/E"側 と strong="R/K"側
  const pair = Object.keys(ANIMALS).filter(k=>ANIMALS[k].quad===quad);
  let animalKey;
  // ie軸の偏りが強ければ ie主導タイプ、rk軸が強ければ rk主導タイプ
  if(ieDiff>=rkDiff){
    animalKey = pair.find(k=>ANIMALS[k].strong===ie) || pair[0];
  }else{
    animalKey = pair.find(k=>ANIMALS[k].strong===rk) || pair[0];
  }
  // 状態判定 (block範囲: -4 〜 +8 / 状態質問4問×-1〜+2)
  let stateKey;
  if(block>=4) stateKey="sleeping";
  else if(block>=1) stateKey="awakening";
  else stateKey="flowing";
  return { animal:ANIMALS[animalKey], state:STATES[stateKey], scores:{I,E,R,K,block} };
}
